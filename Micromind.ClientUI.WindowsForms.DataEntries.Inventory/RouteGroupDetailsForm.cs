		private void dataGridItems_AfterRowActivate(object sender, EventArgs e)
		{
			ValueList valueList = new ValueList();
			valueList.ValueListItems.Add("None");
			foreach (UltraGridRow row in dataGridItems.Rows)
			{
				if (!row.IsActiveRow)
				{
					valueList.ValueListItems.Add(checked(row.Index + 1));
				}
			}
			dataGridItems.ActiveRow.Cells["PreviousStep"].ValueList = valueList;
			if (dataGridItems.ActiveRow.Cells["PreviousStep"].Value.IsNullOrEmpty())
			{
				dataGridItems.ActiveRow.Cells["PreviousStep"].Value = "None";
			}
		}

