		private bool GetData()
		{
			try
			{
				if (currentData == null || isNewRecord)
				{
					currentData = new RouteGroupData();
				}
				DataRow dataRow = (!isNewRecord) ? currentData.RouteGroupTable.Rows[0] : currentData.RouteGroupTable.NewRow();
				dataRow.BeginEdit();
				dataRow["RouteGroupID"] = textBoxCode.Text.Trim();
				dataRow["RouteGroupName"] = textBoxName.Text.Trim();
				dataRow["Note"] = textBoxNote.Text;
				dataRow["Inactive"] = checkBoxInactive.Checked;
				dataRow.EndEdit();
				if (isNewRecord)
				{
					currentData.RouteGroupTable.Rows.Add(dataRow);
				}
				currentData.RouteGroupDetailTable.Rows.Clear();
				foreach (UltraGridRow row in dataGridItems.Rows)
				{
					DataRow dataRow2 = currentData.RouteGroupDetailTable.NewRow();
					dataRow2["RouteGroupID"] = textBoxCode.Text;
					dataRow2["Step"] = int.Parse(row.Cells["Step"].Value.ToString());
					dataRow2["RouteID"] = row.Cells["Route Code"].Value.ToString();
					dataRow2["Description"] = row.Cells["Description"].Value.ToString();
					if (row.Cells["PreviousStep"].Value.ToString() != "None")
					{
						dataRow2["PreviousStep"] = int.Parse(row.Cells["PreviousStep"].Value.ToString());
					}
					else
					{
						dataRow2["PreviousStep"] = DBNull.Value;
					}
					dataRow2["Remarks"] = row.Cells["Remarks"].Value.ToString();
					currentData.RouteGroupDetailTable.Rows.Add(dataRow2);
				}
				return true;
			}
			catch (Exception e)
			{
				ErrorHelper.ProcessError(e);
				return false;
			}
		}

