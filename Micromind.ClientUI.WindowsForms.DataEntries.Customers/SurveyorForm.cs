		private bool GetData()
		{
			try
			{
				if (currentData == null || isNewRecord)
				{
					currentData = new SurveyorData();
				}
				DataRow dataRow = (!isNewRecord) ? currentData.SurveyorTable.Rows[0] : currentData.SurveyorTable.NewRow();
				dataRow.BeginEdit();
				dataRow["SurveyorID"] = textBoxCode.Text.Trim();
				dataRow["SurveyorName"] = textBoxName.Text.Trim();
				dataRow["Tel"] = textBoxTelephone.Text;
				dataRow["Mobile"] = textBoxMobile.Text.Trim();
				dataRow["Email"] = textBoxEmail.Text;
				dataRow["Website"] = textBoxWebsite.Text.Trim();
				dataRow["ContactName"] = textBoxContactName.Text;
				dataRow.EndEdit();
				if (isNewRecord)
				{
					currentData.SurveyorTable.Rows.Add(dataRow);
				}
				return true;
			}
			catch (Exception e)
			{
				ErrorHelper.ProcessError(e);
				return false;
			}
		}

