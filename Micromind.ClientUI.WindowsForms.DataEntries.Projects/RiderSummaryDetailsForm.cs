		public void LoadData(string id)
		{
			try
			{
				if (!base.IsDisposed && !(id.Trim() == "") && CanClose())
				{
					currentData = Factory.RiderSummarySystem.GetRiderSummaryByID(id.Trim());
					if (currentData == null || currentData.Tables.Count == 0 || currentData.Tables[0].Rows.Count == 0)
					{
						ClearForm();
						IsNewRecord = true;
						textBoxCode.Text = id;
						textBoxCode.Focus();
					}
					else
					{
						FillData();
						IsNewRecord = false;
						formManager.ResetDirty();
					}
				}
			}
			catch (Exception e)
			{
				ErrorHelper.ProcessError(e);
			}
		}

		private void FillData()
		{
			if (currentData != null && currentData.Tables.Count != 0 && currentData.Tables[0].Rows.Count != 0)
			{
				DataRow dataRow = currentData.Tables[0].Rows[0];
				if (dataRow["RiderID"] != DBNull.Value)
				{
					textBoxCode.Text = dataRow["RiderID"].ToString();
				}
				if (dataRow["RiderName"] != DBNull.Value)
				{
					textBoxName.Text = dataRow["RiderName"].ToString();
				}
				if (dataRow["LicenseNumber"] != DBNull.Value)
				{
					textBoxLicenseNumber.Text = dataRow["LicenseNumber"].ToString();
				}
				textBoxFEIRegNo.Text = dataRow["FEIRegisterNumber"].ToString();
				textBoxFathersName.Text = dataRow["FathersName"].ToString();
				textBoxFamilyname.Text = dataRow["FamilyName"].ToString();
				comboBoxNationality.SelectedID = dataRow["Nationality"].ToString();
				textBoxContactNumber.Text = dataRow["ContactNumber"].ToString();
				textBoxEmail.Text = dataRow["EMail"].ToString();
				if (dataRow["DateofBirth"] != DBNull.Value)
				{
					dateTimePickerBirthDate.Value = DateTime.Parse(dataRow["DateofBirth"].ToString());
					dateTimePickerBirthDate.Checked = true;
				}
				else
				{
					dateTimePickerBirthDate.Value = DateTime.Now;
				}
				if (dataRow["IsInactive"] != DBNull.Value)
				{
					checkBoxInactive.Checked = bool.Parse(dataRow["IsInactive"].ToString());
				}
				else
				{
					checkBoxInactive.Checked = false;
				}
			}
		}

		private bool SaveData()
		{
			if (!IsDirty)
			{
				if (!IsNewRecord)
				{
					IsNewRecord = true;
					ClearForm();
				}
				return true;
			}
			if (!IsNewRecord)
			{
				switch (ErrorHelper.QuestionMessageYesNoCancel(UIMessages.DoYouWantToSave))
				{
				case DialogResult.No:
					return true;
				case DialogResult.Cancel:
					return false;
				}
			}
			if (!ValidateData())
			{
				return false;
			}
			if (!GetData())
			{
				return false;
			}
			try
			{
				bool flag;
				if (isNewRecord)
				{
					flag = Factory.RiderSummarySystem.CreateRiderSummary(currentData);
					if (flag)
					{
						ComboDataHelper.SetRefreshStatus(DataComboType.JobFee, needRefresh: true);
					}
				}
				else
				{
					flag = Factory.RiderSummarySystem.UpdateRiderSummary(currentData);
				}
				if (!flag)
				{
					ErrorHelper.ErrorMessage(UIMessages.UnableToSave);
				}
				else
				{
					IsNewRecord = true;
					ClearForm();
				}
				return flag;
			}
			catch (Exception e)
			{
				ErrorHelper.ProcessError(e);
				return false;
			}
		}

		private bool ValidateData()
		{
			if (!screenRight.New && isNewRecord)
			{
				ErrorHelper.WarningMessage(UIMessages.NoPermissionNew);
				return false;
			}
			if (!screenRight.Edit && !isNewRecord)
			{
				ErrorHelper.WarningMessage(UIMessages.NoPermissionEdit);
				return false;
			}
			if (!IsNewRecord && !Global.IsUserAdmin && !AllowEditCard && Global.CurrentUser != Factory.SystemDocumentSystem.GetCardUserID("Rider_Summary", "RiderID", textBoxCode.Text))
			{
				ErrorHelper.WarningMessage("You dont have permission to edit (SecurityRoleID:115).");
				return false;
			}
			if (textBoxCode.Text.Trim().Length == 0 || textBoxName.Text.Trim().Length == 0)
			{
				ErrorHelper.InformationMessage("Please enter required fields.");
				return false;
			}
			if (isNewRecord && Factory.DatabaseSystem.ExistFieldValue("Rider_Summary", "RiderID", textBoxCode.Text.Trim()))
			{
				ErrorHelper.InformationMessage("Code already exist.");
				textBoxCode.Focus();
				return false;
			}
			return true;
		}

