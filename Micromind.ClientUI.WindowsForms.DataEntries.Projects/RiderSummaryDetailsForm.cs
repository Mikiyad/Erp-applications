		private bool GetData()
		{
			try
			{
				if (currentData == null || isNewRecord)
				{
					currentData = new RiderSummaryData();
				}
				DataRow dataRow = (!isNewRecord) ? currentData.RiderSummaryTable.Rows[0] : currentData.RiderSummaryTable.NewRow();
				dataRow.BeginEdit();
				dataRow["RiderID"] = textBoxCode.Text.Trim();
				dataRow["RiderName"] = textBoxName.Text.Trim();
				dataRow["IsInactive"] = checkBoxInactive.Checked;
				dataRow["LicenseNumber"] = textBoxLicenseNumber.Text;
				dataRow["FEIRegisterNumber"] = textBoxFEIRegNo.Text;
				dataRow["FathersName"] = textBoxFathersName.Text;
				dataRow["FamilyName"] = textBoxFamilyname.Text;
				dataRow["Nationality"] = comboBoxNationality.Text;
				_ = dateTimePickerBirthDate.Value;
				dataRow["DateofBirth"] = dateTimePickerBirthDate.Value;
				dataRow["Gender"] = comboBoxGender.SelectedID;
				dataRow["ContactNumber"] = textBoxContactNumber.Text;
				dataRow["EMail"] = textBoxEmail.Text;
				dataRow.EndEdit();
				if (isNewRecord)
				{
					currentData.RiderSummaryTable.Rows.Add(dataRow);
				}
				return true;
			}
			catch (Exception e)
			{
				ErrorHelper.ProcessError(e);
				return false;
			}
		}

