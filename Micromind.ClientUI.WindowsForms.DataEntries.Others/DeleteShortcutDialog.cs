		private void DeleteShortcutDialog_Load(object sender, EventArgs e)
		{
			try
			{
				dataGridItems.ApplyUIDesign();
				dataGridItems.DisplayLayout.Override.CellDisplayStyle = CellDisplayStyle.FormattedText;
				ShortcutData shortcuts = Factory.ShortcutSystem.GetShortcuts(1, Global.CurrentUser);
				shortcuts.Tables[0].Columns["ShortcutText"].ColumnName = "Shortcut";
				dataGridItems.DataSource = shortcuts;
				dataGridItems.DisplayLayout.Bands[0].Columns["ShortcutType"].Hidden = true;
				dataGridItems.DisplayLayout.Bands[0].Columns["UserID"].Hidden = true;
				dataGridItems.DisplayLayout.Bands[0].Columns["ShortcutKey"].Hidden = true;
			}
			catch (Exception e2)
			{
				ErrorHelper.ProcessError(e2);
			}
		}

		public string GetSelectedCode(string codeColumnName)
		{
			if (SelectedRow != null)
			{
				return SelectedRow.Cells[codeColumnName].Value.ToString();
			}
			return "";
		}

		private void buttonCancel_Click(object sender, EventArgs e)
		{
			CanClose = true;
			if (this.ItemDeleted != null)
			{
				this.ItemDeleted(this, null);
			}
			if (CanClose)
			{
				Close();
			}
		}

