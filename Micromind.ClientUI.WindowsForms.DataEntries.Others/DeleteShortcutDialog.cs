		private void buttonOK_Click(object sender, EventArgs e)
		{
			if (SelectedRow == null || ErrorHelper.QuestionMessageYesNo("Are you sure to delete this shortcut?") == DialogResult.No)
			{
				return;
			}
			string text = SelectedRow.Cells["ShortcutKey"].Value.ToString();
			string s = SelectedRow.Cells["ShortcutType"].Value.ToString();
			if (Factory.ShortcutSystem.DeleteShortcut(byte.Parse(s), Global.CurrentUser, text))
			{
				if (this.ItemDeleted != null)
				{
					this.ItemDeleted(text, null);
				}
				SelectedRow.Delete(displayPrompt: false);
			}
		}

