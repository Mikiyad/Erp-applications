		private bool GetData()
		{
			try
			{
				if (currentData == null || isNewRecord)
				{
					currentData = new InventoryTransferTypeData();
				}
				DataRow dataRow = (!isNewRecord) ? currentData.InventoryTransferTypeTable.Rows[0] : currentData.InventoryTransferTypeTable.NewRow();
				dataRow.BeginEdit();
				dataRow["TypeID"] = textBoxCode.Text.Trim();
				dataRow["TypeName"] = textBoxName.Text.Trim();
				dataRow["LocationID"] = comboBoxLocation.SelectedID;
				dataRow.EndEdit();
				if (isNewRecord)
				{
					currentData.InventoryTransferTypeTable.Rows.Add(dataRow);
				}
				return true;
			}
			catch (Exception e)
			{
				ErrorHelper.ProcessError(e);
				return false;
			}
		}

		private void buttonSave_Click(object sender, EventArgs e)
